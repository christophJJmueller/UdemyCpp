#include <iostream>

//Regeln zu Variablennamen:
/*
1. Name darf nicht mit einer Zahl starten
2. Name darf keine Leerzeichen enthalten
3. Name muss einzigartig sein
*/

int main()
{
    // Datentyp Variablenname(; = Wert);
    int my_declaration; //Deklaration
    int my_integer_value = 2; //Deklaration + Initialisierung (=erste Wertezuweisung)
    float my_float_value = 3.14;

    return 0;
}
